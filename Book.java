public class Book {
    
    private String name, author, publisher, date, isbn;

    void setName(String setname){
        this.name = setname;
    }

    String getName(){
        return name;
    }

    void setAuthor(String setauthor){
        this.author = setauthor;
    }

    String getAuthor(){
        return author;
    }

    void setPublisher(String setpublisher){
        this.publisher = setpublisher;
    }

    String getPublisher(){
        return publisher;
    }

    void setDate(String setdate){
        this.date = setdate;
    }

    String getDate(){
        return date;
    }

    void setISBN(String setisbn){
        this.isbn = setisbn;
    }

    String getISBN(){
        return isbn;
    }
}